class Main {
  public static void main(String[] args) {
    System.out.println("Hello Java");
    // "文字列"
    // ()の中を出力せよ、という命令
    // javaは文末に;が必須

    // int型の変数
    int number;
    number = 3;
    System.out.println(number);

    // String型の変数
    String name;
    name = "Wanko";
    System.out.println(name);
  }
}